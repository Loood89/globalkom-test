import { HttpClient } from "@angular/common/http";
import { Injectable } from "@angular/core";
import { ReservationT } from "../../../shared/types/reservation.type";
import { Observable, of } from "rxjs";

@Injectable({
  providedIn: "root",
})
export class ReservationsService {
  constructor(private http: HttpClient) {
    const reservations = localStorage.getItem("reservations");
    if (!reservations) {
      localStorage.setItem("reservations", JSON.stringify(FAKE_DATA));
    }
  }

  fetchReservations(): Observable<ReservationT[]> {
    return of(this.getReservationsCmd());
  }

  addReservation(reservation: ReservationT): Observable<ReservationT[]> {
    this.addReservationCmd(reservation);
    return this.fetchReservations();
  }

  deleteReservation(id: number): Observable<ReservationT[]> {
    const reservations = this.getReservationsCmd().filter(
      (item: ReservationT) => {
        return item.id !== id;
      },
    );

    this.setReservationCmd(reservations);

    return this.fetchReservations();
  }

  private getReservationsCmd() {
    const reservations = localStorage.getItem("reservations");
    return !reservations ? [] : JSON.parse(reservations);
  }

  private setReservationCmd(reservations: ReservationT[]) {
    localStorage.setItem("reservations", JSON.stringify(reservations));
  }

  private addReservationCmd(reservation: ReservationT) {
    const reservations = this.getReservationsCmd();
    this.setReservationCmd([
      ...reservations,
      {
        ...reservation,
        id: reservations[reservations.length - 1].id++,
      },
    ]);
  }
}

